
public class CharTest {

	public static void main(String[] args) {
		char ch1 = 'A';
		int i1 = 'A';
		System.out.println(ch1);
		System.out.println(i1);
		//B를 저장하고 출력
		ch1 = 'B';
		i1 = 'B';
		System.out.println(ch1);
		System.out.println(i1);
		ch1 = 'Z';
		i1 = 'Z';
		System.out.println(ch1);
		System.out.println(i1);
		
		//소문자 a, 소문자 z 출력
		ch1 = 'a';
		i1 = 'a';
		System.out.println(ch1);
		System.out.println(i1);
		ch1 = 'z';
		i1 = 'z';
		System.out.println(ch1);
		System.out.println(i1);
	}

}






