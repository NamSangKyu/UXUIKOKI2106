
public class IntVariable {

	public static void main(String[] args) {
		//변수 선언 방법
		//변수타입 변수명;
		int studentAge; //변수를 선언한다.
		studentAge = 20;
		studentAge = 30;
		System.out.println(studentAge);
		
		int n = 10; //변수를 선언과 동시에 초기화(initialize) 하였다.
		System.out.println(n);
		
		int error=0;
		System.out.println(error);
	}

}
