
public class Operator4 {

	public static void main(String[] args) {
		int n1=10, n2 = 15;
		System.out.println(n1 > n2);//false
		System.out.println(n1 < n2);//true
		System.out.println(n1 >= n2);//false
		System.out.println(n1 <= n2);//true
		System.out.println(n1 == n2);//false
		System.out.println(n1 != n2);//true
	}

}
