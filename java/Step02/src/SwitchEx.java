
public class SwitchEx {

	public static void main(String[] args) {
		int n = 5;
		
		switch(n) {
		case 4:
			System.out.println("Four");
			break;
		case 3:
			System.out.println("Three");
			break;
		case 2:
			System.out.println("Two");
			break;
		case 1:
			System.out.println("One");
			break;
		default:
			System.out.println("None");
			
		}
	}

}
