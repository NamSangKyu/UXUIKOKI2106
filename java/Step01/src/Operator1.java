
public class Operator1 {

	public static void main(String[] args) {
		// 사칙연산자 : + - * / %
		int n1 = 10, n2 = 7;
		int result = n1 + n2;
		System.out.println(result);
		result = n1 - n2;
		System.out.println(result);
		// 곱셈, 나눗셈, 나머지 연산자
		result = n1 * n2;
		System.out.println(result);
		result = n1 / n2;
		System.out.println(result);
		result = n1 % n2;
		System.out.println(result);
		
		result = n1 + 10 * n2;
		System.out.println(result);

	}

}




